        <p class="title center">员工内部推荐奖励制度</p>



<p class="title">一、总则</p>

<p>此制度仅适用于公司全体在职员工（人力资源部员工除外）</p>



<p class="title">二、目的</p>

<p>为了进一步调动公司内部员工队伍的人脉优势，提升内部员工荐优举贤的积极性，实现公司人力资源的最佳配置，拓宽公司招聘渠道，鼓励员工举荐人才，提高招聘工作效率和质量。</p>



<p class="title">三、推荐的原则</p>

<p class="title">1、回避原则</p>

1.1内部举荐的应聘人员不包括与举荐人或本公司在职人员存在直系亲属关系；<br/>

1.2被推荐人员不能从事与推荐人在工作上有互相利益关系的岗位；<br/>

1.3被推荐人的亲属不能有从事与本公司经营范围一致的工作或在该类公司里工作。<br/>

<p class="title">2、内部员工推荐人选，必须遵守以下原则：</p>

2.1 所有举荐人的身份学历工作经验必须是真实的 ；<br/>

2.3 推荐人提供的被推荐人简历应该准确真实，不能有协助被推荐人做任何隐瞒和伪造，否则将取消其推荐候选人的资格；<br/>

2.4 如被推荐人员有任何刑事犯罪记录，或者其他严重影响工作的疾病或传染性疾病，推荐人需如实向公司申明，不可故意隐瞒；<br/>

2.5 被推荐人员与其他公司无劳动用工及经济纠纷。<br/>

<p class="title">3、遵守原则</p>

3.1 主管级及以上员工推荐同系统同部门内的主管级以下员工，推荐人选须通过人力资源部及用人部门正规面试程序，但是人员比例由人力资源部进行合理控制；



<p class="title">四、内部推荐流程</p>

<p class="title">1、 空缺岗位公布</p>

人力资源部定期在公司群发布《内部推荐岗位公告》，包括招聘岗位信息、招聘周期、奖励金额等。

<p class="title">2、 推荐人选</p>

2.1推荐人可到人力资源部领取《公司内部人才推荐表》并填写相关信息，该表格将做为奖金发放的依据，如未填写可能会影响到奖金的发放；<br/>

2.2推荐人依据相关招聘岗位的工作职责及职位要求，将被推荐人的简历以书面材料或电子文档形式发送至人力资源部同事邮箱（hrzhp@xgrobotics.com），并在邮件标题或正文上注明推荐人+推荐职位；

<p class="title">3、资料审核</p>

人力资源部收到推荐资料后，在2个工作日内对资料进行初步审核，并通知推荐人；

<p class="title">4、正常招聘</p>

通过审核的应聘者，公司人力资源部及相关用人部门组织进行初试及复试，5天内确定是否录用，确定录用人员后，人力资源部告知推荐人。





<p class="title">五、奖励制度说明</p>

<p class="title">1、奖励制度</p>

1.1如员工推荐的候选人符合空缺职位的要求，且已通过最终面试，进入试用期或至转正后，推荐人获取相应现金奖励；<br/>

1.2如果员工推荐的候选人被公司录用并顺利经过试用期成为正式员工，推荐人获取相应全额现金奖励。

<p class="title">2、此制度范围</p>

2.1推荐人享受的奖金金额，按照被推荐人试用期通过后的级别和职位给予；<br/>

2.2如员工推荐的候选人不符合空缺职位要求，推荐人不享受任何奖励；<br/>

2.3被推荐人选如果未被录用,其应聘资料将进入公司人才库作为储备；<br/>

2.4全体员工适用，总监级（含）以上中高管推荐人才至本部门工作的除外。



<p class="title">六、奖金确认及发放流程</p>

<p class="title">1、奖金发放流程及标准</p>

1.1被推荐人员获聘用并转正后，按下列规定给予推荐人一定的奖励，人事部提交，经相关领导审批，审批通过后，随当月工资一同发放；<br/>

1.2被推荐人入职一个月后支付推荐人推荐奖的30%，转正后第二个月内支付推荐奖的70%。

